// NULL & UNDEFINED
// Null
// "Intentional absence of any value"
// Must be assigned


let something = null;
console.log(something); //null
something = "nothing";
console.log(something);


// Undefined
// Variables that do not have an
// assigned value are undefined

let name; //undefined
console.log(name); // undefined
name = "Josh";
console.log(name);
